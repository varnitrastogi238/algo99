import numpy as np

def algo_max(np1,np2):
    return np.maximum(np1,np2)

def algo_min(np1,np2):
    return np.minimum(np1,np2)

def algo_floor(np1):
    return np.floor(np1)

def algo_ceil(np1):
    return np.ceil(np1)

def algo_abs(np1):
    return np.abs(np1)

def algo_round(np1):
    return np.round(np1)

def algo_lcm(np1,np2):
    return np.lcm(np1,np2)

def algo_gcd(np1,np2):
    return np.gcd(np1,np2)


